int	limits_int_check(int *stack_a, int argc)
{
	int	i;

	i = argc - 2;
	while (i > 0)
	{
		if (stack_a[i] > 2147483647 || stack_a[i] < -2147483648)
		{
			write (1, "Error\n", 6);
			return (0);
		}
		i++;
	}
	return (0);
}
